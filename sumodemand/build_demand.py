#!/usr/bin/env python3
"""Build calibrated SUMO demand (.rou.xml) for sumotwin from GDOT portal
Turning Movement Count reports.

Source of truth: data/<code>/{date}data.txt -- the portal's own tabular
5-min per-movement (L, T) vehicle counts. This replaces the chart-digitizer
pipeline (atspm_digitizer `tmc` mode) as the demand source: data.txt is
exact integer counts straight from the portal, not a pixel-traced
approximation, so there's no digitization error and no rendering gaps
(every 5-min bin is populated, even at zero volume). See
sumodemand/portal_report.py for the parser. Only dates with a full-day
(288-bin) data.txt are built; partial exports are skipped with a warning
(see main()).

Fidelity policy (CLAUDE.md): raw values only, no rescaling/smoothing.
Applied here as: an exact <flow number="..."> straight from the portal's
integer count per bin -- no vph->count rounding step, since data.txt is
already a count, not a rate.

OD -> lane mapping: derived directly from the auto-generated <connection>
elements in sumotwin/7065.net.xml (netconvert's real lane channelization,
see the printed connection dump / sumotwin/README.md). A <flow> only needs
an edge-level <route>; SUMO's junction connections + lane-changing model
already restrict each movement to its real lane(s) -- e.g. EB-thru only
uses SW_in lanes 0-2, EB-left only lanes 3-4 -- so no explicit per-lane
assignment is needed on the flow itself.

Known gap: right-turn movements exist as real lanes in the network
(NB-right S_in->NE_out, SB-right N_near->SW_out, EB-right SW_in->S_out,
the last one sharing SW_in lane 0 with EB-thru) but GDOT never reports them
for this intersection (no R column/movement in data/7065/*data.txt for any
date). Per fidelity policy these are left at zero flow rather than
fabricated -- see the printed caveat and sumodemand/README.md.
"""
import pathlib

import portal_report

REPO = pathlib.Path(__file__).resolve().parents[1]
REPORT_DIR = REPO / "data" / "7065"
OUT_DIR = REPO / "sumodemand"

APPROACHES = ["nb", "sb", "eb", "wb"]
MOVEMENTS = ["thru", "left"]

# (approach, movement) -> edge sequence in sumotwin/7065.net.xml
# NB inbound = S_in (from south leg, heading north); SB inbound = N_far->N_near
# (from north leg, heading south); EB inbound = SW_in (from SW leg, heading ENE);
# WB inbound = NE_in->NE_in.15->NE_in.79 (from NE leg, heading WSW). See
# sumodemand/README.md.
#
# WB is 3 edges, not 1: sumotwin's slip-lane rework (see sumotwin/README.md)
# split NE_in at the point where the traced free-right slip diverges
# (NE_in -> NE_in.15 -> NE_in.79 -> J), so any route continuing straight/left
# through the signal must list all three segments. Only the thru/left demand
# built here uses that path; the slip lane itself (NE_in -> NE_in.15 -> E1 ->
# N_out.37, a real right-turn movement that bypasses the signal) carries no
# demand -- like the other three approaches' right turns, it was never
# digitized for this intersection (see the "Known gap" print at the bottom).
ROUTE_EDGES = {
    ("nb", "thru"): ["S_in", "N_out"],
    ("nb", "left"): ["S_in", "SW_out"],
    ("sb", "thru"): ["N_far", "N_near", "S_out"],
    ("sb", "left"): ["N_far", "N_near", "NE_out"],
    ("eb", "thru"): ["SW_in", "NE_out"],
    ("eb", "left"): ["SW_in", "N_out"],
    ("wb", "thru"): ["NE_in", "NE_in.15", "NE_in.79", "SW_out"],
    ("wb", "left"): ["NE_in", "NE_in.15", "NE_in.79", "S_out"],
}


def build_day(report: portal_report.PortalReport):
    date = report.date
    out_path = OUT_DIR / f"{date}.rou.xml"
    lines = [
        '<?xml version="1.0" encoding="UTF-8"?>',
        "<!-- Calibrated demand for sumotwin, built from the GDOT portal's "
        f"turning movement count report for {date} (2026). Generated by "
        "sumodemand/build_demand.py; do not hand-edit, regenerate instead. -->",
        '<routes xmlns:xsi="http://www.w3.org/2001/XMLSchema-instance" '
        'xsi:noNamespaceSchemaLocation="http://sumo.dlr.de/xsd/routes_file.xsd">',
    ]

    for (approach, movement), edges in ROUTE_EDGES.items():
        route_id = f"{approach}_{movement}"
        lines.append(f'    <route id="{route_id}" edges="{" ".join(edges)}"/>')

    # SUMO requires <flow>/<vehicle> elements to appear in nondecreasing
    # begin-time order across the WHOLE file, not just within one movement's
    # own block -- so collect every flow first and sort globally before
    # writing, rather than emitting movement-by-movement.
    flow_entries = []  # (begin, xml_line)
    log = []
    for approach in APPROACHES:
        for movement in MOVEMENTS:
            route_id = f"{approach}_{movement}"
            bins = report.counts[(approach, movement)]
            n_zero = sum(1 for c in bins.values() if c == 0)
            n_emitted = 0
            daily_total_veh = 0
            for begin, count in sorted(bins.items()):
                daily_total_veh += count
                if count <= 0:
                    continue
                end = begin + portal_report.BIN_SECONDS
                fid = f"{date}_{route_id}_{begin}"
                flow_entries.append((
                    begin,
                    f'    <flow id="{fid}" route="{route_id}" begin="{begin}" '
                    f'end="{end}" number="{count}"/>',
                ))
                n_emitted += 1

            log.append(
                f"  {approach}-{movement:5s}: emitted {n_emitted:3d}/{len(bins):d} bins "
                f"({n_zero} zero) | exact portal daily total = {daily_total_veh:6d} veh"
            )

    flow_entries.sort(key=lambda e: e[0])
    lines.extend(line for _, line in flow_entries)
    lines.append("</routes>")
    out_path.write_text("\n".join(lines) + "\n")
    return log


def main():
    OUT_DIR.mkdir(parents=True, exist_ok=True)
    all_reports = portal_report.discover_reports(REPORT_DIR)
    if not all_reports:
        raise SystemExit(f"no *data.txt files found under {REPORT_DIR}")

    reports = [r for r in all_reports if r.is_full_day]
    skipped = [r for r in all_reports if not r.is_full_day]
    for r in skipped:
        print(f"skipping {r.path.name}: only {r.n_bins}/{portal_report.N_BINS} "
              f"bins present (partial export)")
    if skipped:
        print()

    for report in reports:
        print(f"=== {report.date} ===")
        for line in build_day(report):
            print(line)
        print()

    print(
        "Known gap: NB-right (S_in->NE_out), SB-right (N_near->SW_out), "
        "EB-right (SW_in->S_out), and WB-right (the traced free-right slip, "
        "NE_in->NE_in.15->E1->N_out.37, which bypasses the signal) all have "
        "real lanes in sumotwin/7065.net.xml but no portal TMC report data for "
        "this intersection -- left at zero flow, not fabricated."
    )


if __name__ == "__main__":
    main()
